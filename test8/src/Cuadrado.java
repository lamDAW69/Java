public class Cuadrado {
    private double lado;

    public Cuadrado(double lado) {      //Constructor
        this.lado = lado;
    }
    public Cuadrado() {      //Constructor sin parametros
        this.lado = lado;
    }
    public void setLado(double lado) { //Setter
        this.lado = lado;
    }

    public double getLado() {        //Getter
        return lado;
    }
}
